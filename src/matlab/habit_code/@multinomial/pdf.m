% MULTINOMIAL/PDF
%
function p = pdf(model,x);

%******************************************************************************
%   Find indices of x in domain
%******************************************************************************
[inDomain domainIndice] = ismember(x,model.v);

%******************************************************************************
%   Allocate space for result
%******************************************************************************
p = zeros(size(x));

%******************************************************************************
%   For all values in domain, return the referenced probability
%******************************************************************************
p(inDomain) = model.p(domainIndice);


